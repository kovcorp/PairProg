package calculator;

public class Calculator {

    public long addTwoNumbers(int a, int b) {
        return a + b;
    }

    public long substractNnumbers(int a, int b) {
        return a - b;
    }

    public long multipleNumbers(int a, int b) {
        return a * b;
    }

    public double divideNumbers(double a, double b) {
        return a / b;
    }
}